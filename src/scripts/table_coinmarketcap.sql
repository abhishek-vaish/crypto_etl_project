CREATE TABLE RAW.COINMARKETCAP (
	ID INT PRIMARY KEY IDENTITY(1, 1),
	RANK INT NOT NULL,
	NAME VARCHAR(255) NOT NULL,
	SYMBOL VARCHAR(50) NOT NULL,
	TYPE VARCHAR(255),
	CATEGORY VARCHAR(255),
	IMAGE VARCHAR(255),
	MARKETCAP VARCHAR(255) NOT NULL,
	PRICE VARCHAR(255) NOT NULL,
	CIRCULATINGSUPPLY VARCHAR(255) NOT NULL,
	TOTALSUPPLY VARCHAR(255) NOT NULL,
	MAXSUPPLY VARCHAR(255) NOT NULL,
	VOLUME VARCHAR(255) NOT NULL,
	PERCENTCHANGEHOUR VARCHAR(50) NOT NULL,
	PERCENTCHANGEWEEK VARCHAR(50) NOT NULL,
	PERCENTCHANGEDAY VARCHAR(50) NOT NULL,
	PERCENTCHANGEMONTH VARCHAR(50) NOT NULL,
	DAYHIGH VARCHAR(50) NOT NULL,
	DAYLOW VARCHAR(50) NOT NULL,
	LASTUPDATED DATETIME,
	FILEDATE DATETIME,
	CREATEDAT DATETIME
);