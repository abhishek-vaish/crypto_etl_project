CREATE TABLE RAW.COINMARKETCAP (
	ID INT PRIMARY KEY IDENTITY(1, 1),
	RANK INT NOT NULL,
	NAME VARCHAR(255) NOT NULL,
	SYMBOL VARCHAR(50) NOT NULL,
	TYPE VARCHAR(255),
	CATEGORY VARCHAR(255),
	IMAGE VARCHAR(255),
	MARKETCAP VARCHAR(255) NOT NULL,
	PRICE VARCHAR(255) NOT NULL,
	CIRCULATINGSUPPLY VARCHAR(255) NOT NULL,
	TOTALSUPPLY VARCHAR(255),
	MAXSUPPLY VARCHAR(255),
	VOLUME VARCHAR(255) NOT NULL,
	PERCENTCHANGESIXMIN VARCHAR(50),
	PERCENTCHANGEWEEK VARCHAR(50),
	PERCENTCHANGEDAY VARCHAR(50),
	PERCENTCHANGEMONTH VARCHAR(50),
	DAYHIGH VARCHAR(50),
	DAYLOW VARCHAR(50),
	LASTUPDATED DATETIME,
	FILEDATE DATETIME,
	CREATEDAT DATETIME
);